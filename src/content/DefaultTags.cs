        public static List<TagData> FuzzySearch(string search, int returnAmount = 16)
        {
            if(returnAmount <= 0) {  throw new ArgumentOutOfRangeException(nameof(returnAmount)); };

            string[] searchList = search.Split(" ");
            if (searchList.Length == 0) { return DEFAULT_TAG_DATA.Take(returnAmount).ToList(); }
            string guess = searchList[searchList.Length - 1];

            List<TagData> matches = DEFAULT_TAG_DATA
                .OrderByDescending(x => Fuzz.Ratio(x.Name, guess))
                .Take(Math.Min(DEFAULT_TAG_DATA.Count, returnAmount)).ToList();

            return matches;
